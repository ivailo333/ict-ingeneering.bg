„бисквитки“ от този Уебсайт?</B></SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">Всички
браузъри позволяват управлението на
„бисквитки“ от специално създадена
за целта папка на Вашия браузър. Можете
да блокирате получаването на „бисквитки“,
да изтриете всички или част от тях или
да зададете Вашите настройки за
предпочитания по отношение на използването
на „бисквитки“ преди да инициирате
посещение на сайта ни. Имайте предвид,
че изтриването или блокирането на
„бисквитки“ може да повлияе неблагоприятно
върху функциите на нашия Уебсайт, а
оттам и върху Вашето потребителско
преживяване на него.</SPAN></FONT></FONT></P>
