„бисквитките“ се управлява от настройките
на Вашия браузер. Имайте предвид, че
пълната забрана на използването на
всички „бисквитки“, може да рефлектира
върху функционалното представяне на
сайта, неговата ефективност и достъпването
на определена информация.</SPAN></FONT></FONT></P>
