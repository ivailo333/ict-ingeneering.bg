<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.14in; line-height: 115%"><A NAME="docs-internal-guid-32ce269e-7fff-b0e5-f8"></A>
<SPAN LANG="bg-BG"><BR></SPAN><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>Използване
на „бисквитки“</B></SPAN></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000">„</FONT><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">Бисквитките“
са кратки текстови файлове или малки
пакети от информация, които се съхраняват
чрез Интернет браузера на Вашето крайно
устройство (компютър, таблет, лаптоп
или мобилен телефон), когато посещавате
различни сайтове и страници в Интернет.
Основната цел на „бисквитките“ е да
правят потребителя разпознаваем, когато
той се завръща отново на Уебсайта. Някои
„бисквитки“ имат и по-специфично
приложение, като например да запаметяват
потребителското поведение на сайта и
да улесняват потребителя при ползването
на Уебсайта. Повече информация за това
как функционират „бисквитките“ можете
да намерите в Интернет.</SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>Как
се използват „бисквитки“ на този
Уебсайт?</B></SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">Ние
използваме „бисквитки“ на този Уебсайт
предимно с цел улесняване на ползваемостта
на сайта, подобряване на неговата работа
и съхраняване на информация за
потребителското поведение. При този
процес не се съхраняват никакви лични
данни, т.е. чрез “бисквитките“ на сайта
не може да Ви идентифицираме като
личност, поради което спрямо събирането
на тази информация не се прилага Закона
за защита на личните данни. Събраната
информация от „бисквитки“ обикновено
се използва в обобщен вид с цел анализ
на потребителското поведение на Уебсайта,
което ни позволява да подобряваме
функционалността на сайта, потребителските
пътеки и ползваното съдържание.</SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>Какви
„бисквитки“ се използват на този
Уебсайт?</B></SPAN></FONT></FONT></P>
<P LANG="bg-BG"><BR><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>Сесийни
„бисквитки“</B></SPAN></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">Този
тип „бисквитки“ Ви улеснява в ползването
на сайта, като те съхраняват информация
временно, само в рамките на сесията на
използвания браузер. Обикновено
информацията, която се съхранява чрез
тях, е какви стоки или услуги сте добавили
в количката, кои страници на сайта сте
посетили и как сте стигнали до дадена
информация. Тези „бисквитки“ не събират
информация от Вашето крайно устройство
и се изтриват автоматично, когато
напуснете Уебсайта или прекратите
сесията на Вашия браузър.</SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>Постоянни
&quot;бисквитки&quot;</B></SPAN></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><A NAME="_GoBack"></A><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">Те
ни дават възможност да съхраняваме
конкретна информация за сърфирането,
като например анализиране посещенията
на сайта, как вие сте достигнали до
Уебсайта, какви страници сте прегледали,
какви опции сте избрали, както и накъде
сте се насочили през този Уебсайт.
Проследяването на тази информация, ни
дава възможност да правим подобрения
на Уебсайта, включително да коригираме
грешки и да разширяваме съдържанието.
Срокът на съхранение на този вид
“бисквитки” варира според конкретното
им предназначение.</SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG"><B>&quot;Бисквитки&quot;
на трета страна</B></SPAN></FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><FONT COLOR="#000000"><FONT FACE="Cambria, serif"><SPAN LANG="bg-BG">На
нашия Уебсайт има препратки към други
сайтове или вградено съдържание от
други сайтове, например от Facebook, YouTube,
Twitter, Google+, LinkedIn, уебсайтове на партньори.
Възможно е при посещаването на тези
сайтове или отварянето на съдържанието
от тях, да се складират на Вашето крайно
устройство „бисквитки“ от тези
уебсайтове. Именно тези „бисквитки“
се дефинират като „бисквитки на трети
страни“, като нямаме контрол върху
генерирането и управлението на тези
„бисквитки“. За това Ви съветваме да
потърсите информация за тях и за начина
им на управление на уебсайтовете на
съответните трети страни.</SPAN></FONT></FONT></P>
<P LANG="bg-BG" ALIGN=JUSTIFY STYLE="margin-bottom: 0in"><BR>
